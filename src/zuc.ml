let l2 x = Int32.logxor x (Int32.logxor (rotl32 x 8) (Int32.logxor (rotl32 x 14) (Int32.logxor (rotl32 x 22) (rotl32 x 30))))

let f ctx x0 x1 x2 =
  let w = Int32.add (Int32.logxor x0 ctx.r1) ctx.r2 in
  let w1 = Int32.add ctx.r1 x1 in
  let w2 = Int32.logxor ctx.r2 x2 in
  ctx.r1 <- sbox_word (l1 (Int32.logor (Int32.shift_left w1 16) (Int32.shift_right_logical w2 16)));
  ctx.r2 <- sbox_word (l2 (Int32.logor (Int32.shift_left w2 16) (Int32.shift_right_logical w1 16)));
  w

let lfsr_step ctx u_opt =
  let s16 = ref ctx.s.(0) in
  s16 := add31 !s16 (mul31 ctx.s.(0) 8);
  s16 := add31 !s16 (mul31 ctx.s.(4) 20);
  s16 := add31 !s16 (mul31 ctx.s.(10) 21);
  s16 := add31 !s16 (mul31 ctx.s.(13) 17);
  s16 := add31 !s16 (mul31 ctx.s.(15) 15);
  (match u_opt with None -> () | Some u -> s16 := add31 !s16 u);
  let s16 = if Int32.equal !s16 0l then mask31 else !s16 in
  for i = 0 to 14 do
    ctx.s.(i) <- ctx.s.(i + 1)
  done;
  ctx.s.(15) <- s16

let load1 a b c =
  Int32.logor
    (Int32.shift_left (Int32.of_int a) 23)
    (Int32.logor (Int32.shift_left (Int32.of_int b) 8) (Int32.of_int c))

let init ~key ~iv =
  if Bytes.length key <> 16 || Bytes.length iv <> 16 then invalid_arg "Zuc.init";
  let s = Array.make 16 0l in
  for i = 0 to 15 do
    s.(i) <- load1 (Char.code (Bytes.get key i)) d.(i) (Char.code (Bytes.get iv i))
  done;
  let ctx = { s; r1 = 0l; r2 = 0l } in
  for _ = 0 to 31 do
    let x0, x1, x2, _ = bit_reorg ctx.s in
    let w = f ctx x0 x1 x2 in
    lfsr_step ctx (Some (Int32.shift_right_logical w 1))
  done;
  let x0, x1, x2, _ = bit_reorg ctx.s in
  ignore (f ctx x0 x1 x2);
  lfsr_step ctx None;
  ctx

