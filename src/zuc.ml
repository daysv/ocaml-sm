let keystream_words t n =
  if n < 0 then invalid_arg "Zuc.keystream_words";
  Array.init n (fun _ -> next_word t)

let crypt t input =
  let out = Bytes.create (Bytes.length input) in
  let i = ref 0 in
  while !i < Bytes.length input do
    let w = next_word t in
    let ks = Bytes.create 4 in
    Bytes.set ks 0 (Char.chr (Int32.to_int (Int32.shift_right_logical w 24) land 0xff));
    Bytes.set ks 1 (Char.chr (Int32.to_int (Int32.shift_right_logical w 16) land 0xff));
    Bytes.set ks 2 (Char.chr (Int32.to_int (Int32.shift_right_logical w 8) land 0xff));
    Bytes.set ks 3 (Char.chr (Int32.to_int w land 0xff));
    let take = min 4 (Bytes.length input - !i) in
    for j = 0 to take - 1 do
      Bytes.set out (!i + j) (Char.chr (Char.code (Bytes.get input (!i + j)) lxor Char.code (Bytes.get ks j)))
    done;
    i := !i + take
  done;
  out

let crypt_with_key ~key ~iv input = crypt (init ~key ~iv) input
